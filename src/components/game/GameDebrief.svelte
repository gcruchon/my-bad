<div class="debrief">
  <h3>Aide au débrief</h3>
  <h4>Rappeler les notions clés</h4>

  <dt class="error">Erreur</dt>
  <dd>
    Une erreur est
    <em>un acte unitaire regrettable</em>
    qui consiste à se tromper, à dévier de la norme ou de la «bonne» façon de
    faire (couper le mauvais serveur en production, se tromper dans un calcul,
    envoyer un email au mauvais destinataire, etc.).
  </dd>

  <dt class="mistake">Faute</dt>
  <dd>
    Une faute est
    <em>une transgression - volontaire ou par négligence - d'une règle</em>
    sur laquelle la communauté s'était mise d'accord, voire, idéalement, que
    cette communauté a co-construite. La faute peut avoir plusieurs degrés (une
    faute simple, une faute grave ou une faute lourde, dans le droit du
    travail).
  </dd>

  <dt class="failure">Échec</dt>
  <dd>
    Un échec consiste à
    <em>ne pas arriver au résultat attendu</em>. Il est possible d’échouer après
    avoir commis une ou plusieurs erreurs. Il existe aussi des formes d’échec
    qui ne sont pas liées, en tant que tel, à l’acte de la personne qui échoue
    (dépasser le nombre d’incidents majeurs fixé par la DSI, augmenter le taux
    de détraction d’un produit, ne pas faire passer les bons messages à son
    équipe...).
  </dd>

  <h4>Poser des questions ouvertes</h4>
  <ul>
    <li>Qu'avez-vous pensé du jeu ?</li>
    <li>Qu'avez-vous appris ?</li>
    <li>Est-ce que certaines questions vous ont génées? Si oui, lequelles ?</li>
    <li>
      Que peut-on améliorer (sur le jeu, sur les questions) ? Qu'aurait-on pu
      faire mieux ?
    </li>
    <li>Quelles sont les éléments qui sont encore flous ?</li>
  </ul>

  <h4>Faire un « ROTI »</h4>
  <p>
    Return On Time Invested, il s'agit de demander à chaque membre de l'équipe
    de noter, entre 1 et 4, le temps passé lors de ce jeu. Une note de « 4 »
    j'ai appris des choses et que le temps passé m'a été très utile. Une note de
    « 1 » si je n'ai rien gagné, rien appris.
  </p>
  <p>
    Vous pourrez utiliser
    <a
      href="https://roti.express/?lang=fr"
      title="Roti.express"
      target="_blank">Roti.express</a>, par exemple.
  </p>

  <h4>Débriefer sur la liberté de parole autour de l'erreur</h4>
  <p>
    Il s'agit ici de discuter sur la manière dont les membres de l'équipe
    appréhendent l'erreur. Sont-ils à l'aise pour en discuter ? Qu'est-ce que
    peut les retenir d'en parler et d'apprendre de leurs erreurs ?
  </p>
